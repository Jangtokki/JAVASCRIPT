//최대값
console.log(Math.max(1, 2, 3, 4, 5)); //5

//최소값
console.log(Math.min(1, 2, 3, 4, 5)); //1

//절대값
console.log(Math.abs(-10)); //10

//반올림
console.log(Math.round(25.3333)); //25
console.log(Math.round(25.3333, 1)); //25

//올림
console.log(Math.ceil(23.23333)); //24

//내림
console.log(Math.floor(23.77667)); //23

//난수 0.0이상 ~ 1.0미만
console.log(Math.random());

//반올림
console.log((25.523).toFixed(2));
console.log((25.526).toFixed(2));
